/**
 * 玩家"主动退场"意图检测（客户端）。
 *
 * 设计前提：检测**永远只用来弹确认框**，绝不直接结束 session。所以误判的最坏
 * 代价是"弹个框、玩家点继续"，永不焊死一局——命中策略因此只需为精度调（少打扰），
 * 不必为安全调（数据由确认框兜底）。
 *
 * 三道闸（全过才算想退场），这里用"整句锚定正则"一次性表达：
 *  1. 短而独立：模式用 ^...$ 锚定整句，长叙事/嵌入用法自然不匹配。
 *  2. 命中元词典：只认游戏世界里不会出现的元游戏词（退出 / 不玩了 / 结束游戏 …），
 *     故意**不收**"结束 / 离开 / 走 / 算了"这类剧情高频词。
 *  3. 无剧情标记：含人名、引号、逗号的台词/叙事整句匹配不上 ^...$，被天然挡掉。
 *
 * 漏判（真想退却没识别）= 退回现状（当普通输入进引擎，无副作用）；
 * 误判（剧情被当退出）= 弹个确认框点继续。两个方向都不致命。
 */

const EXIT_PATTERNS: RegExp[] = [
  // 退出 / 我要退出 / 退出游戏 / 退出吧
  /^(我)?(要|想)?退出(游戏|吧)?$/,
  // 不玩了 / 我不玩了
  /^(我)?不玩了$/,
  // 不想玩 / 不想玩了 / 我不想玩游戏
  /^(我)?不想玩(了|游戏)?$/,
  // 不想再玩(了) / 不想继续(了)
  /^(我)?不想(再玩|继续)(了)?$/,
  // 结束游戏 / 结束这局 / 结束这一局 / 结束本局 / 我想结束游戏(吧)
  /^(我)?(想)?结束(这一?局|本局|游戏)(游戏)?(了|吧)?$/,
  // 关掉游戏 / 关闭游戏
  /^(我)?(要)?(关掉|关闭)游戏$/,
  // 英文
  /^(quit|exit)$/i,
];

/** 去掉首尾空白与首尾标点（"退出。" / "我不玩了！" 仍应命中）。 */
function normalize(raw: string): string {
  return raw
    .trim()
    .replace(/^[\s。.!！?？、,，…~～]+/, "")
    .replace(/[\s。.!！?？、,，…~～]+$/, "")
    .trim();
}

export function isExitIntent(raw: string): boolean {
  if (!raw) return false;
  const text = normalize(raw);
  if (!text) return false;
  return EXIT_PATTERNS.some((re) => re.test(text));
}
